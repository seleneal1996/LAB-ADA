def CuadraditosPD(M):
  m=len(M)
  n=len(M[0])
  dp= [0]*(n+1)
  Mmin=0 
  cont=0
  for i in range(m):
    for j in range(n):
      if M[i][j]!=0:
        cont+=1
        Mmin=(M[i-1][j]>M[i-1][j-1]) if M[i-1][j-1] else M[i-1][j]
        Mmin=(M[i][j-1]>Mmin) if Mmin else M[i][j-1]
        M[i][j]=Mmin+1
      for cont in range(2,Mmin+1):
        dp[cont]+=1
  print('Hay:',cont,'cuadrado de:',1,'x',1)
  for i in range(2,n+1):
    if dp[i] != 0:
      print('Hay:',dp[i],'cuadrados de:',i,'x',i)
    else:
      print('Hay:',dp[i],'cuadrados de:',i,'x',i)

